export default function HertfordshireMap() {
  const mapRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    if (!mapRef.current) return;

    const map = L.map(mapRef.current, {
      center: [51.7963, -0.0789],
      zoom: 10,
      zoomControl: false,
      attributionControl: false,
      dragging: false,
      scrollWheelZoom: false,
      doubleClickZoom: false,
      boxZoom: false,
      keyboard: false,
    });

    L.tileLayer("https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png", {
      maxZoom: 18,
    }).addTo(map);

    L.polygon(HERTS_BOUNDARY, {
      color: "#ef4444",
      weight: 3,
      fillColor: "#ef4444",
      fillOpacity: 0.06,
      dashArray: "6, 6",
    }).addTo(map);

    L.marker([51.7963, -0.0789], {
      icon: L.divIcon({
        className: "custom-pin",
        html: `<div style="width:16px;height:16px;background:#ef4444;border-radius:50%;border:3px solid white;box-shadow:0 2px 6px rgba(0,0,0,0.3);"></div>`,
        iconSize: [16, 16],
        iconAnchor: [8, 8],
      }),
    }).addTo(map);

    return () => {
      map.remove();
    };
  }, []);

  return (
    <div
      ref={mapRef}
      style={{ width: "100%", height: "100%", filter: "grayscale(1) contrast(1.25) brightness(0.75)" }}
    />
  );
}
